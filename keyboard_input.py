print("please give me input like w, a, s, d ")

while True:
    
    key = input("give me input")

    if key == "w":
        print("w")
        
    elif key == "a":
        print("a")

    elif key == "s":
        print("s")

    elif key == "d":
        print("d")
        
    else:
        print("wrong input")
        break